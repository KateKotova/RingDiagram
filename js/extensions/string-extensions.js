; (
	/**
	 * Contains the extensions for the global String object.
	 * @module string-extensions
	 * @author Kate Kotova <katekotova_86@mail.ru>
	 * @copyright Kate Kotova 2017
	 */
	function ( window ) {
		'use strict';

		/**
		 * An empty string.
		 * @global
		 */
		String.EMPTY = "";
		/**
		 * Space.
		 * @global
		 */
		String.SPACE = " ";		
		/**
		 * Openning parenthesis.
		 * @global
		 */
		String.OPENNING_PARENTHESIS = "(";
		/**
		 * Closing parenthesis.
		 * @global
		 */
		String.CLOSING_PARENTHESIS = ")";
		/**
		 * Comma.
		 * @global
		 */
		String.COMMA = ",";
		/**
		 * Percent.
		 * @global
		 */
		String.PERCENT = "%";
		/**
		 * Dash.
		 * @global
		 */
		String.DASH = "-";		
		
	}( window )
);